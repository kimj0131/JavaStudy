public class A0_Hello {
	
	// main() : 프로그램 그자체
	//			메인이 시작되면 프로그램이 시작되고
	//			메인이 끝나면 프로그램이 끝난다
	
	public static void main(String[] args) {
		// 프로그램의 시작
		
		// System.out.println();
		
		// 괄호에 전달한 값을 콘솔화면에 출력하라는 명령어
		// 콘솔 : 글자만 나오는 컴퓨터 화면
		
		// ;(세미콜론)
		// - 한 명령어가 끝났음을 알리는 문자
		System.out.println("Hello, World!");
		System.out.println("김");
		System.out.println("!");
		System.out.println("11asd");
		
		// 프로그램의 끝
	}
	
}
